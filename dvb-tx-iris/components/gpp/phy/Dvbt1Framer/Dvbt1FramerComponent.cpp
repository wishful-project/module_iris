#define	T1_PIL_AMPL			1.333333333333F
#define	T1_TPS_AMPL			1.0F

#define	T1_N_BCH				127
#define	T1_K_BCH				113

/// This functions generates the modulated TPS carriers
///
/// @param tps Preallocated array that will contain the bit to be transmitted
///            in the TPS carriers
/// @param block_in_frame Index of the block in the current frame (0...67)
/// @param frame_in_superframe Index of the frame in the current superframe (0...3)
/// @return 0 if all went well, else errors happened
int Dvbt1FramerComponent::t1_tps_generate(unsigned char *tps, int block_in_frame,
    int frame_in_superframe)
{
	int i = 0, j = 0;
	unsigned char feedback = 0;
	unsigned char x[T1_K_BCH] = {0}, b[T1_N_BCH - T1_K_BCH] = {0};

	// Code generator polynomial
	static unsigned char g[] = {1, 1, 1, 0, 1, 1, 1, 0, 1, 1, 0, 0, 0, 0, 1};


	// Predefined TPS bits

	// Very first bit is given by PRBS and it is used
	// as a reference in differential BPSK modulation
	// The present value is set to zero, but it reall
	// doesn't care                                  
	tps[0] = 0;

	// Synchronization
	switch(frame_in_superframe)
  {
		// Frame 1 and 3
	case 0:
	case 2:
		tps[1]  = 0;
		tps[2]  = 0;
		tps[3]  = 1;
		tps[4]  = 1;
		tps[5]  = 0;
		tps[6]  = 1;
		tps[7]  = 0;
		tps[8]  = 1;
		tps[9]  = 1;
		tps[10] = 1;
		tps[11] = 1;
		tps[12] = 0;
		tps[13] = 1;
		tps[14] = 1;
		tps[15] = 1;
		tps[16] = 0;
		break;
		// Frame 2 and 4
	case 1:
	case 3:
		tps[1]  = 1;
		tps[2]  = 1;
		tps[3]  = 0;
		tps[4]  = 0;
		tps[5]  = 1;
		tps[6]  = 0;
		tps[7]  = 1;
		tps[8]  = 0;
		tps[9]  = 0;
		tps[10] = 0;
		tps[11] = 0;
		tps[12] = 1;
		tps[13] = 0;
		tps[14] = 0;
		tps[15] = 0;
		tps[16] = 1;
		break;
	}

	// TPS length indicator
	// Full DVB-H option wants 33 bits
	// NOT USED NOW !!!
	if(cellId_x >= 0)
  {
		// Cell Id is set, 31 bits
		tps[17] = 0;
		tps[18] = 1;
		tps[19] = 1;
		tps[20] = 1;
		tps[21] = 1;
		tps[22] = 1;
	}
  else
  {
		// Cell Id is not set, 23 bits
		tps[17] = 0;
		tps[18] = 1;
		tps[19] = 0;
		tps[20] = 1;
		tps[21] = 1;
		tps[22] = 1;
	}

	// Variable TPS bits

	// Counts the frame number in superframe
	switch(frame_in_superframe)
  {
	case 0:
		tps[23] = 0;
		tps[24] = 0;
		break;
	case 1:
		tps[23] = 0;
		tps[24] = 1;
		break;
	case 2:
		tps[23] = 1;
		tps[24] = 0;
		break;
	case 3:
		tps[23] = 1;
		tps[24] = 1;
	}

	// Constellation
	switch(qamMapping_x)
  {
		// QPSK
	case 4:
		tps[25] = 0;
		tps[26] = 0;
		break;
		// 16-QAM
	case 16:
		tps[25] = 0;
		tps[26] = 1;
		break;
		// 64-QAM
	case 64:
		tps[25] = 1;
		tps[26] = 0;
		break;
	}

	// In-depth inner interleaver
	if(inDepthInterleaver_x == false)
	{
		// Native
		tps[27] = 0;
	}
	else
	{
		tps[27] = 1;
	}
	
	// Hierarchy
	switch(hyerarchyMode_x)
  {
		// Not hierarchical
	case 0:
		tps[28] = 0;
		tps[29] = 0;
		break;
		// Alpha = 1
	case 1:
		tps[28] = 0;
		tps[29] = 1;
		break;
		// Alpha = 2
	case 2:
		tps[28] = 1;
		tps[29] = 0;
		break;
		// Alpha = 4
	case 4:
		tps[28] = 1;
		tps[29] = 1;
		break;
	}

	// Code rate
	if(hyerarchyMode_x == 0)
  {
		// Code rate, NH
		switch (hpCodeRate_x) {
		case 12:
			tps[30] = 0;
			tps[31] = 0;
			tps[32] = 0;
			break;
		case 23:
			tps[30] = 0;
			tps[31] = 0;
			tps[32] = 1;
			break;
		case 34:
			tps[30] = 0;
			tps[31] = 1;
			tps[32] = 0;
			break;
		case 56:
			tps[30] = 0;
			tps[31] = 1;
			tps[32] = 1;
			break;
		case 78:
			tps[30] = 1;
			tps[31] = 0;
			tps[32] = 0;
			break;
		default:
			break;
		}
	}
  else
  {
		// Code rate, HP
		switch(hpCodeRate_x)
    {
		case 12:
			tps[30] = 0;
			tps[31] = 0;
			tps[32] = 0;
			break;
		case 23:
			tps[30] = 0;
			tps[31] = 0;
			tps[32] = 1;
			break;
		case 34:
			tps[30] = 0;
			tps[31] = 1;
			tps[32] = 0;
			break;
		case 56:
			tps[30] = 0;
			tps[31] = 1;
			tps[32] = 1;
			break;
		case 78:
			tps[30] = 1;
			tps[31] = 0;
			tps[32] = 0;
			break;
		default:
			break;
		}

		// Code rate, LP
		switch(lpCodeRate_x)
    {
		case 12:
			tps[33] = 0;
			tps[34] = 0;
			tps[35] = 0;
			break;
		case 23:
			tps[33] = 0;
			tps[34] = 0;
			tps[35] = 1;
			break;
		case 34:
			tps[33] = 0;
			tps[34] = 1;
			tps[35] = 0;
			break;
		case 56:
			tps[33] = 0;
			tps[34] = 1;
			tps[35] = 1;
			break;
		case 78:
			tps[33] = 1;
			tps[34] = 0;
			tps[35] = 0;
			break;
		default:
			break;
		}
	}

	// Guard interval
	switch(deltaMode_x)
  {
		case 32:
			tps[36] = 0;
			tps[37] = 0;
			break;
		case 16:
			tps[36] = 0;
			tps[37] = 1;
			break;
		case 8:
			tps[36] = 1;
			tps[37] = 0;
			break;
		case 4:
			tps[36] = 1;
			tps[37] = 1;
			break;
	}

	// Transmission mode
	switch (ofdmMode_x)
  {
	case 2048:
		tps[38] = 0;
		tps[39] = 0;
		break;
	case 8192:
		tps[38] = 0;
		tps[39] = 1;
		break;
	case 4096:
		tps[38] = 1;
		tps[39] = 0;
		break;
	}

	// Cell identification bits
	if(cellId_x >= 0)
  {
		// Compute Cell Id bits
		unsigned char cidv[16];
		for(i = 0; i < 16; i++)
			cidv[i] = (unsigned char) ((((unsigned short int) cellId_x) >> i) & 0x0001);

		switch(frame_in_superframe)
    {
			// First half in frames 1 and 3
			case (0):
			case (2):
				for (i = 40; i <= 47; i++)
					tps[i] = cidv[55 - i];
				break;

			// Second half in frames 2 and 4
			case (1):
			case (3):
				for (i = 40; i <= 47; i++)
					tps[i] = cidv[47 - i];
				break;
		}
	}
  else
  {
		// Cell Id not set
		for(i = 40; i <= 47; i++)
			tps[i] = 0;
	}

	// Bits 48 and 49 are used in DVB-H
	// Not set currently
	tps[48] = 0;
	tps[49] = 0;

	// Bits from 50 to 53 are all set to zero
	tps[50] = 0;
	tps[51] = 0;
	tps[52] = 0;
	tps[53] = 0;

	// BCH encoding

	// Empty the parity register
	for(i = 0; i < (T1_N_BCH - T1_K_BCH); i++)
		b[i] = 0;

	// Reverse copy data into x, considering the shortening zeroes
	for(i = 53; i > 0; i--)
		x[53 - i] = tps[i];
	
	// Compute redundacy bb[], the coefficients of b(x). The redundancy
	// polynomial b(x) is the remainder after dividing x^(n-k)*data(x)
	// by the generator polynomial g(x).
	for(i = (T1_K_BCH - 1); i >= 0; i--)
  {
		feedback = x[i] ^ b[T1_N_BCH - T1_K_BCH - 1];
		if(feedback != 0)
    {
			for(j = (T1_N_BCH - T1_K_BCH - 1); j > 0; j--)

				if (g[j] != 0)
					b[j] = b[j - 1] ^ feedback;
				else
					b[j] = b[j - 1];

			b[0] = g[0] && feedback;

		}
    else
    {
			for(j = (T1_N_BCH - T1_K_BCH - 1); j > 0; j--)
				b[j] = b[j - 1];

			b[0] = 0;
		}
	}

	/* Back copy parity bits into s */
	for(i = 0; i < 14; i++)
		tps[54 + i] = b[13 - i];

	return 0;
}

/// Main processing method
void Dvbt1FramerComponent::process()
{
  // request input
  DataSet< Cplx > *in = NULL;
  getInputDataSet("input1", in);

  // calculate sizes
  int insize = in ? (int) in->data.size() : 0;
  int outsize = kMax_ * ((insize + fraOffset_) / nMax_);
  
  // request output
  DataSet< Cplx >* out = NULL;
  getOutputDataSet("output1", out, outsize);
 		
  // print debug info
  if(debug_x)
    LOG(LINFO) << "in/out: " << insize << "/" << outsize;
    
  // fill register
  for(CplxVecIt init = in->data.begin(), outit = out->data.begin();
    init < in->data.end(); init++)
  {
    // copy
    fraRegister_[fraOffset_++] = *init;
    
    // ready for new block - trigger
    if(fraOffset_ == nMax_)
    {
      // reset offset
      fraOffset_ = 0;
      	    
      // initial position values
      int scatt_pil_pos = 3 * (blockIndex_ & 0x03);
      int cpp = 0;
      int cont_pil_pos = cont_pilot_position[cpp];
      int tp = 0;
      int tps_pos = tps_position[tp];
      
      // counters
      int frameInSuperFrame = blockIndex_ / T1_BLOCKS_PER_FRAME;
      int blockInFrame = blockIndex_ - frameInSuperFrame * T1_BLOCKS_PER_FRAME;
		
      // generate the tps information for this frame
      if(blockInFrame == 0)
      {
        int status = t1_tps_generate(tps_, blockInFrame, frameInSuperFrame);
        if(status)
          LOG(LERROR) << "Error in TPS parity generation";
      }
      
      // populate the frame
      CplxVecIt regit = fraRegister_.begin();
		  for(int k = 0; k < kMax_; k++, outit++)
		  {
			  if(k == scatt_pil_pos) {
				  // scattered pilot
				  outit->real(prbs_pilot[k] ? -T1_PIL_AMPL : T1_PIL_AMPL);
				  outit->imag(0);
				  scatt_pil_pos += 12;
				  if (k == cont_pil_pos)
				  {
					  // coincidence with continual pilot
					  cont_pil_pos = cont_pilot_position[++cpp];
				  }
			  } 
			  else if(k == cont_pil_pos)
			  {
				  // continual pilot
				  outit->real(prbs_pilot[k] ? -T1_PIL_AMPL : T1_PIL_AMPL);
				  outit->imag(0);
				  cont_pil_pos = cont_pilot_position[++cpp];
			  }
			  else if(k == tps_pos)
			  {
				  // TPS
				  // first symbol in frame: absolute reference for differential encoding
				  if (blockInFrame == 0)
				  {
					  tpsAmpl_[k] = prbs_pilot[k] ? -T1_TPS_AMPL : T1_TPS_AMPL;
				  }
				  else
				  {
					  // subsequent symbols in frame
					  // differentially encoded bits with respect to the first frame bits
					  tpsAmpl_[k] = tps_[blockInFrame] ? -tpsAmpl_[k] : tpsAmpl_[k]; 
				  }
				  outit->real(tpsAmpl_[k]);
				  outit->imag(0);
				  
		      
				  tps_pos = tps_position[++tp];
			  }
			  else
			  {
				  // real data
				  *outit = *regit++;
			  }
		  }
		  
		  
		  // advance block index
		  if(++blockIndex_ == T1_BLOCKS_PER_FRAME * T1_FRAMES_PER_SUPERFRAME)
		  {
		    // reset
		    blockIndex_ = 0;
		  }
    }
  }
                
  // Copy the timestamp and sample rate for the DataSets
  out->timeStamp = in->timeStamp;
  out->sampleRate = in->sampleRate;

  // release input and output
  releaseInputDataSet("input1", in);
  releaseOutputDataSet("output1", out);
}

/// Actions taken when the parameters change
/// 
/// This block has several significant parameters
void Dvbt1FramerComponent::parameterHasChanged(std::string name)
{
  if(name == "hpcoderate" || name == "deltamode" ||
     name == "lpcoderate" || name == "qammapping" ||
     name == "hyerarchymode" || name == "ofdmmode" ||
     name == "cellid" || name == "indepthinterleaver")
  {
    destroy();
    setup();
  }
}

/// Set up all needed constants
void Dvbt1FramerComponent::setup()
{
  // prepare
  blockIndex_ = 0;
  fraOffset_ = 0;
  switch(ofdmMode_x)
  {
    case 2048:
      kMax_ = 1705;
      nMax_ = 1512;
      break;
    case 4096:
      kMax_ = 3409;
      nMax_ = 3024;
      break;
    case 8192:
      kMax_ = 6817;
      nMax_ = 6048;
      break;
  }
  fraRegister_.resize(nMax_);
  memset(tpsAmpl_, 0, sizeof tpsAmpl_);
  memset(tps_, 0, sizeof tps_);
}

