/// Main processing method
void Dvbt1SymbolInterleaverComponent::process()
{
  // request input
  DataSet< uint8_t > *in = NULL;
  getInputDataSet("input1", in);

  // calculate sizes
  int insize = in ? (int) in->data.size() : 0;
  int outsize = siLength_ * ((insize + siOffset_) / siLength_);
  
  // request output
  DataSet< uint8_t >* out = NULL;
  getOutputDataSet("output1", out, outsize);
 		
  // print debug info
  if(debug_x)
    LOG(LINFO) << "in/out: " << insize << "/" << outsize;
    
  // symbol by symbol
  for(ByteVecIt init = in->data.begin(), outit = out->data.begin(); init < in->data.end(); init++)
  {
    // copy to register
    siRegister_[siOffset_++] = *init;
    
    // trigger interleaving
    if(siOffset_ == siLength_)
    {
      // reset offset
      siOffset_ = 0;

      // actual interleaving matrix
      int *H = ofdmMode_x == 2048 ? H_2K_ : (ofdmMode_x == 4096 ? H_4K_ : H_8K_); 
        
      // copy
      switch(eo_++ & 0x01)
      {
        case 0:
          // even numbered symbol
          for(int s = 0; s < siLength_; s++)
            outit[H[s]] = siRegister_[s];
          break;
        case 1:
          // odd numbered symbol
          for(int s = 0; s < siLength_; s++)
            outit[s] = siRegister_[H[s]];
          break;
      }
      
      // advance output
      outit += siLength_;
    }
  }
        
  // Copy the timestamp and sample rate for the DataSets
  out->timeStamp = in->timeStamp;
  out->sampleRate = in->sampleRate;

  // release input and output
  releaseInputDataSet("input1", in);
  releaseOutputDataSet("output1", out);
}

/// Actions taken when the parameters change
/// 
/// This block has one significant parameter
void Dvbt1SymbolInterleaverComponent::parameterHasChanged(std::string name)
{
  if(name == "ofdmmode")
  {
    destroy();
    setup();
  }
}

/// Set up the register space and initialize
void Dvbt1SymbolInterleaverComponent::setup()
{
  // clean
  eo_ = 0;
  siOffset_ = 0;
  siLength_ = ofdmMode_x == 2048 ? 1512 : (ofdmMode_x ==  4096 ? 3024 : 6048);
  
  // get room
  siRegister_ = new uint8_t [siLength_];
}

/// Destroy the component
void Dvbt1SymbolInterleaverComponent::destroy()
{
  // clean
  delete [] siRegister_;
}

